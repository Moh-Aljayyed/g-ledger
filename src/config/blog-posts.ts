// Get today's post
export function getTodayPost(): BlogPost | null {
  const today = new Date().toISOString().split("T")[0];
  return BLOG_POSTS.find((p) => p.date === today) || BLOG_POSTS[BLOG_POSTS.length - 1];
}

// Get posts up to today
export function getPublishedPosts(): BlogPost[] {
  const today = new Date().toISOString().split("T")[0];
  return BLOG_POSTS.filter((p) => p.date <= today).reverse();
}
